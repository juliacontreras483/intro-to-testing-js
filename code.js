// sayHello function

function sayHello(input) {
    if (input === true || input === false) {
        return ("Hello, World!")
    } else if (input === "Pat") {
        return "Hello, Pat!";
    } else if (input === "Alex") {
        return "Hello, Alex!";
    } else {
        return "Hello, Jane!";
    }
}


